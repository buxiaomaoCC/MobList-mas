package com.example.myapplication.bean;

public class ListviewObject {
    private Integer listview_iv;
    private String tv_name;
    private String tv_price;

    public Integer getListview_iv() {
        return listview_iv;
    }

    public void setListview_iv(Integer listview_iv) {
        this.listview_iv = listview_iv;
    }

    public String getTv_name() {
        return tv_name;
    }

    public void setTv_name(String tv_name) {
        this.tv_name = tv_name;
    }

    public String getTv_price() {
        return tv_price;
    }

    public void setTv_price(String tv_price) {
        this.tv_price = tv_price;
    }
}
